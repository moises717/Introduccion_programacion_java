public class TiposPrimitivosChar {
   public static void main(String[] args) {
    
       // imprecion de caracter
       char miCaracter = 'a';
       System.out.println("mi caracter = " + miCaracter); // salidad a
       
       char varChar = '\u0021';
       System.out.println("var unicode" + varChar); // salidad !
       
       char varCharDecimal = 33;
       System.out.println("decimal = " + varCharDecimal); // salidad !
       
       char varCharSimbolo = '!';
       System.out.println("simbolo " + varCharSimbolo);
       
       char varCharSimbolo3 = '!';
       System.out.println("var char 3 " + varCharSimbolo3); // salida 
       
       int variableEnteraSimbolo = '!';
       System.out.println("variableEnteraSimbolo = " + variableEnteraSimbolo);
       
       int letra = 'A';
       System.out.println("letra = " + letra);
    
}
            
    
}
